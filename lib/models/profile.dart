  late String email;
  late String password;
  late String name;
  late String surname;
  late String birthday;
  late String gender;
  late String weight;
  late String height;

  Profile({
    required this.email,
    required this.password,
    required this.name,
    required this.surname,
    required this.birthday,
    required this.gender,
    required this.weight,
    required this.height,
  });
}
